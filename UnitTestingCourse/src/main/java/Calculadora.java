
public class Calculadora {
	public static double soma(double valor1, double valor2) {
		return valor1 + valor2;
	}

	public static double subtrai(double valor1, double valor2) {return valor1 - valor2;}

	public static double divide(double valor1, double valor2){return valor1 / valor2;}

	public static double multiplica(double valor1, double valor2) {return valor1 * valor2;}

}
